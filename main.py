import sys

from PyQt5.QtWidgets import QApplication
from ui.main_window import MainWindow


# ==========================================================
# ТОЧКА ВХОДА В ПРИЛОЖЕНИЕ
# ==========================================================

def main():
    """
    Основная функция запуска графического приложения.

    Здесь создаётся:
    - экземпляр QApplication (управляет жизненным циклом GUI);
    - главное окно приложения;
    - запускается цикл обработки событий.
    """

    # Создание приложения Qt
    # sys.argv позволяет передавать аргументы командной строки
    app = QApplication(sys.argv)

    # Создание главного окна программы
    window = MainWindow()

    # Отображение окна на экране
    window.show()

    # Запуск основного цикла обработки событий Qt
    # (программа "зависает" здесь и ждёт действий пользователя)
    sys.exit(app.exec_())


# ==========================================================
# ЗАПУСК ПРОГРАММЫ
# ==========================================================

if __name__ == "__main__":
    # Эта проверка гарантирует,
    # что код выполнится только при запуске файла напрямую,
    # а не при импорте как модуля
    main()